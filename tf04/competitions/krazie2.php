What do you think about a house that floats on water? A floating habitat, floating villages, floating cities, if you had already dismissed it as 'just' another crazy idea, well then, its your turn to be surprised, 'coz that's what designers, builders and architects are racking their brains on in the Netherlands. More than 2/3 of this European country is below the sea level.  With the sea level rising as a result of global warming, this idea is gaining unprecedented acceptance as an ideal solution to the progressive space-problem being faced in the Netherlands. For centuries the Netherlands has fought against
invading water with land fills, dams and dykes. But now the Dutch have found out a better way to solve the problem. Instead of trying to drive out the water, the Dutch are finding ways of living on it. Well, all that means that we've found another scheme to solve the ubiquitous space-problem on earth. Also you get the added advantage of being able to move your house wherever you fancy. However, there are many technical and
environmental feasibilities to be taken into account before the whole idea becomes a pragmatic reality.<br><br>
 This year Krazie Idea at Techfest 2004 invites you to investigate this
exciting prospect of LIVING ON WATER.
