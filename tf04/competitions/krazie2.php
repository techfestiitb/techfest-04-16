<p><strong><a name=prob><font color=#293932 ><b>PROBLEM STATEMENT</b></font></a></strong><br><br>
 Conceptualize and design a self sustained colony (with a minimum of 20 and a maximum of 100 people) floating in an ocean/sea.
</p>
<br><br>
<p><strong><a name=obj><font color=#293932 ><b>OBJECTIVE </b></font></a></strong>
<br><br>
This competition provides an opportunity to the participants to harness their creative prowess along with the designing skills. The participants are expected to conceptualize a colony for human being that stably floats on a natural water body and is self sustained to the maximum possible extent. Apart from the technological and safety aspects, humanitarian considerations also form a part of a good design.
</p>
<br><br>
