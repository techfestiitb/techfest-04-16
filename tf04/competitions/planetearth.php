Welcome to India, it's a lovely place. A country with a 1000 million, the population isn't sparse. One of the most serious issues concerning the common man in the rural areas is the mode of transport. It not only has to be low on investment but the cost on maintenance also needs to be just about zero. Plus, come rain or shine, the mode of transport has to go on ... a long distance. <br><br>
A cycle is a very convenient, cheap and effective solution to this issue. It goes a long way in helping the common man shuttle between his home, workplace and other nodes. It.s an all-terrain, all-road transportation means with no fuel requirements. It also comes as a zero pollution and low maintenance alternative. <br><br>
However, it lacks on the comfort and ergonomics front. Basically, it does not protect the rider from the scorching sun, the chilly wind or the thunderous rain.
