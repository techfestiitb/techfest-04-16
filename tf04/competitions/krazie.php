</p><p>
  Most new ideas in science which are at odds with the paradigm of time
are considered to be crazy. To put the point across, let us consider a
few examples of how some freaky ideas changed the course of the world.
</p><p>
  Consider, for example, Nicolas Copernicus (1473-1543) idea that the
Earth orbits the Sun and not vice-versa. This was thought to be
ludicrous by contemporary astronomers and clearly opposed the intuition
and common sense of majority of the society. But, in the end, it is
common knowledge as to who had the last laugh on this issue.
</p><p>
  Almost all the ingenious ideas proposed have been met with scorn and
mockery. Yet another famous success story is of the Wright brothers,
Orville and Wilbur Wright, who <b>dared to dream</b> the most intrepid 
dream of
their time---that of air travel. Their brainchild was the aeroplane; now 
the most often used mode of trans-continental transport.
</p><p>
  Coming to 1945, another phenomenal notion which was to revolutionize
the
world was first mooted by the science fiction writer Arthur C. Clarke --
that of satellite technology. If trying to send things a thousand miles
away was a crazy idea, satellite broadcasting is its outcome.
</p><p>
  So, here's an opportunity for every one of you to harness your creative
prowess, and show the world what you are capable of. Techfest 2004 is
proud to present <b>Krazie Idea</b>; a platform for budding innovators 
and
trailblazers to <b>create the future</b>. Think the unthinkable!
Anything.....the crazier, the better! But of course like everything
else, we must insist order among this disorder. Your idea must be
reasonably feasible considering the technicalities and economics
involved. A problem statement will be floated very soon.
</p>
