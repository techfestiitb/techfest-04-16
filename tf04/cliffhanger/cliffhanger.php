<p><strong><a name=prob><font color=#293932 ><b>PROBLEM STATEMENT</b></font></a></strong>
<blockquote> 
  <p>A rectangular plateau (2m x 1.5m) rests on a cliff 1.5m high. This plateau is divided into 2 regions by a boundary 
at the centre and has 10 balloons placed in each region (see figure below). Make a machine that climbs up the 1.5m high 
cliff with the help of freely hanging rope (see specifications) and bursts the balloons placed on the plateau. 
Two competing machines start on the task simultaneously and the one that bursts all balloons in the minimum time or maximum number of balloons in the allotted 5 minutes, which ever happens earlier, will be declared winner. In case, none of the machines competing are able to complete the task, the machine ahead in the game will be declared winner.
</p>
</blockquote>
