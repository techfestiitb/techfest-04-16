<br>
Techfest is the annual science and technology festival of Indian Institute of Technology (IIT), Bombay, India. Techfest 
2004 is scheduled for 24th-26th January 2004 at Mumbai, India and promises a myriad of events like Competitions, 
Workshops, Lecture Series and entertainment shows. With its inception in 1998 to being the largest of it's kind in 
entire Asia in 2003, Techfest has come a long way. In its seventh avatar, Techfest 2004 extends its horizons to reach 
out to students not only from India but across the world. We introduce <b>Cliffhanger</b> - the rope-climbing machine 
design contest!

<br><br>

To scale heights has always been a challenge for the mankind … be it the chilly heights of Mt. Everest or simply a few 
storeys of a building on fire! And every such situation urges mankind to look for newer options to tame the challenge. Machines have come long way in doing what was once thought to be impossible. Yet again, we look for machines that can scale heights and find application in day to day problems like fire-fighting, disaster management and rescue operations. Cliffhanger presents a simplified situation that addresses this issue as the problem statement of the contest.  
<br><br>

Entries for Cliffhanger are invited from any school 
student or undergraduate/postgraduate student from across the world. The 
participants would be
classified into 2 categories - <i> India</i> and<i> Rest of the World </i>- for the qualifying round. The teams
clearing this qualifying round shall attend the finals to be held in Mumbai, India during Techfest 2004 (24th- 26th
January, 2004). A few select qualifying teams from the Rest of the World category will also receive to and fro airfare
for the finals. The winning teams from both the categories combined would receive cash prizes of a total of US $ 2500.
Please see the links below for complete details of the contest.

