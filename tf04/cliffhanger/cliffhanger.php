<p><strong><a name=genrules><font color=#293932><b>GENERAL 
RULES</b></font></a></strong><br>
  <br>
  <strong><font color="#7f8068">&nbsp;&nbsp;&nbsp;Sensors And Electronics:</font></strong>
  
<blockquote>The teams can use sensors (if they so desire) to sense any physical 
  parameters but be sure that TV lights do not interfere with your sensors' activities. 
  For the autonomous robot machine, you may/may not use sensors and microprocessors 
  - the decision is yours. The organizers will not provide you with any system 
  like microprocessor kits, computers and so on at the venue.<br>
  <br></blockquote>
<strong><font color="#7f8068">&nbsp;&nbsp;&nbsp;Power Supply:</font></strong>
<blockquote> Normal 230 V extension boards will be available at the venue. You 
  should fetch your own eliminators/adaptors. No specialized demand shall be entertained. 
  Please do not demand heavy equipment like compressors. Use of devices that can 
  transfer manual power directly to machine (example: use of clutch wire) is not 
  allowed. The power cords connecting a machine to the power supply must be slack 
  at all times. Likewise the Control Cords must also be slack. So, please ensure 
  that all cords are fairly long.<br>
  <br>
  <br></blockquote>
 <strong>&nbsp;&nbsp;&nbsp;<font color="#7f8068">Safety:</font></strong><br>
<blockquote> The machines you build will be first checked for size and safety. 
  The machine should not cause electric power breakdowns. The robots must not 
  contain any combustible, corrosive, or otherwise dangerous materials for safety 
  reasons. No explosive compression or decompression, either internal or external 
  is permitted. There is a risk of compressed containers rupturing and creating 
  shrapnel. Any robot whose strategy or operation is considered too dangerous 
  shall be disqualified by the judges at their discretion.<br>
  <br></blockquote>
 <strong><font color="#7f8068">&nbsp;&nbsp;&nbsp;Breakdowns/Timeouts:</font></strong><br>
<blockquote> There is no provision for breakdown or timeouts in this game. Your 
  machine must be sturdy enough to participate in several matches. <br>
  <br></blockquote>
<strong>&nbsp;&nbsp;&nbsp;<font color="#7f8068">Fouls/Penalties:</font></strong>
<blockquote> 
  <ol>
    <li>Team members are not permitted to touch either their machines or those 
      of their opponents once a game begins. The penalty for doing so is disqualification. 
    </li>
    <li>The robots are not allowed to dismember themselves or leave parts on the 
      platform.</li>
    <li>Machines damaging the platform will be disqualified. </li>
  </ol>
</blockquote>
<strong>&nbsp;&nbsp;&nbsp;<font color="#7f8068">Machine Names:</font></strong><br><blockquote>
The name of your machine must be prominently displayed on the machine.<br>
<br></blockquote>
 <strong><font color="#7f8068">&nbsp;&nbsp;&nbsp;Sportive and Friendly 
Games:</font></strong><br>
<blockquote> Intentional damage to the opponent's machine is not allowed. Goodwill 
  and friendship between competitors may evaporate if, for example, one opponent 
  uses lightening bolts to destroy the electronics of the competitor.<br>
</blockquote>
<strong><font color="#7f8068">&nbsp;&nbsp;&nbsp;Scratching a team:</font></strong> <br>
<blockquote> Any team that is not ready to play at the time specified will be 
  scratched automatically. This applies to Eliminations, Prelims and Finals rounds.</blockquote><br>
<strong><font color="#7f8068">&nbsp;&nbsp;&nbsp;Demonstration to 
Judges</font></strong><font color="#7f8068"><strong>:</strong></font><br>
<blockquote> Machines will have to be demonstrated to judges (who will evaluate 
  the machines for other prizes) at a time and date specified in the schedule 
  which will be put up. Any team that does not turn up at the specified time will 
  not be considered for the above special prizes.</blockquote><br>
<strong><font color="#7f8068">&nbsp;&nbsp;&nbsp;Demonstration of Machines at 
Final:</font></strong> 
<blockquote> The Finals will be conducted in front of a large audience. Teams 
  which have lost in the Prelims will be required to demonstrate their machines 
  (one minute per machine) to the audience at that time. A group photograph of 
  all the machines/participants will be taken. The date, time and venue will be 
  specified at the time of competition.<br>
  <br>
</blockquote>
<p>The organizers reserve all rights to change any or all of the above rules as 
  they deem fit. The decision of the judges and the organizers will be final and 
  binding.</p>
