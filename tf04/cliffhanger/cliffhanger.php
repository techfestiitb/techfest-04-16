<p><strong><a name=grule><font color=#293932><b>GAME RULES</b></font></a></strong><blockquote>
  <p>Spirit of this game is what matters. But there are a few formal restrictions that need to be adhered to. Below mentioned are guidelines which every competitor 
should follow. In case of any ambiguity in design of machine or in understanding
the rule/guidelines, please contact the organisers at the address mentioned below for clarifications.
</p> 
  </p>
  <p><strong></strong></p>
  <ol>
    <li> All students enrolled in high school or undergraduate/postgraduate programme at their institute are 
      eligible to participate in Cliffhanger at Techfest 2004.<br>
      <br>
    </li>
    <li>A team can consist of a maximum of three students.<br>
      <br>
    </li>
    <li>The machine should fit within a box of dimensions (10 x 10 x 10) inch at 
      any and every given point of the game.<br>
      <br>
    </li>
    <li> The machine must use only mechanical power or mechanical power converted 
      from a source of electrical energy, chemicals, compressed gas and rockets. 
      Pre-tensioned springs (<font color=red>for lifting the 
machine</font>) or combustion power method are strictly not allowed.<br>
      <br>
    </li>
    <li> Lift assisting devices like balloons are not permitted. Before starting, 
      the device must be demonstrated to be at full mechanical rest potential 
      as far as the structure will allow.<br><br>
    </li>
    <li> The competitor will be allowed to engage the rope to his/her machine 
      at the beginning of the game. You are not allowed to touch your machine 
      once the game starts.<br>
      <br>
    </li>
    <li>The machine should climb up using only the rope and the support of the 
      wall.<br>
      <br>
    </li>
    <li> After reaching the top, the machine should completely disengage itself 
      from the rope before it starts bursting the balloons.<br>
      <br>
    </li>
    <li> Ten balloons will be randomly placed inside each compartment. The machine 
      which bursts more number of balloons wins.<br>
      <br>
    </li>
    <li> You are not allowed to burst the balloons unless the machine reaches 
      the plateau and completely disengages itself from the rope.<br>
      <br>
    </li>
    <li>Use of chemicals, fire or similar harmful things to burst the balloons 
      is prohibited.<br>
      <br>
    </li>
    <li> During the game if a balloon goes outside the board or in the opposite 
      compartment, it will be placed in its respective compartment by the judges 
      only.<br>
      <br>
    </li>
    <li>Duration of a game is 5 minutes. In case one of the machines completes 
      its task (reaches the top and bursts all the 10 balloons in its compartment) 
      before time, the game ends and that machine will be declared the winner.<br>
      <br>
    </li>
    <li>If both the machines fail to complete the given task within the time limit, 
      the machine which has burst a greater number of balloons will be declared 
      the winner. <br>
      <br>
    </li>
    <li>In case both machines fail to burst even one balloon after reaching the 
      top, the machine that reached the top first shall be declared the winner.<br>
      <br>
    </li>
    <li>In case both machines fail to reach the top, the machine which has climbed 
      more distance shall be declared the winner.<br>
      <br>
    </li>
    <li> If both machines have burst equal balloons or climbed an equal distance, 
      the game ends in a tie. In the case of a tie, the game will restart.<br>
      <br>
    </li>
    <li>If a machine gets stuck or falls down from the rope during the game, the 
      competitors are not allowed to touch it till the game is over. 
<br><br>
    </li>
  <li><font color=red>Gearbox and/or motor assembly of a toy 
car, or a 
base of a toy car with its gear box and/or motor as a machine 
part will disqualify the machine for the competition.</font><br></li>
  </ol>
</blockquote>

<strong>&nbsp;&nbsp;&nbsp;<font color="#7f8068">Structure of the 
Competition</font></strong><font color="#7f8068">:</font>
<blockquote> 
  <ol>
    <li> The tournament will be played on a knockout basis.<br>
      <br>
    </li>
    <li> There shall be a preliminary qualification cum seeding round just before the competition 
      for all the competing teams. The time limit to qualify will be three minutes.<br>
      <br>
    </li>
    <li>In this seeding round, each team will have to climb the wall in a maximum 
      time of three minutes. The time taken by the machine will decide its seeding 
      for the first round of the competition. The first and the last teams will 
      be paired, second and the second last and so on. <br>
      <br>
    </li>
    <li>The teams will be paired similarly in the next round and so on till the 
      finals.<br>
      <br>
    </li>
    <li>If any team is disqualified for any reason, the opponent team will get 
      a walkover to the next round.<br>
      <br>
    </li>
    <li> The teams have to be ready for the contest within two minutes of the 
      announcement for them to appear for their match, failing which, the judges 
      can disqualify that team.<br><br>
    </li>
    <li> The teams must adhere to the spirit of healthy competition.<br>
      <br>
    </li>
    <li>Judges reserve the right to modify the rules if they deem it necessary 
      in order to preserve the spirit of healthy competition.<br>
      <br>
    </li>
  </ol>
</blockquote>
<strong><font color="#7f8068">&nbsp;&nbsp;&nbsp;Grand Finale:</font></strong> 
<blockquote> 
  <ol>
    <li>The Grand Finale shall consist of best-of-three rounds.<br>
      <br>
    </li>
    <li> If the task is completed (i.e. machine climbs the cliff and at least 
      one of the balloons is burst) in a round, then the winner of the round shall 
      be awarded 1 point and the loser 0.<br>
      <br>
    </li>
    <li>If both machines fail to complete the task in a given round, then the 
      winner of this round will be awarded 0.5 and the loser 0. The winner in 
      such a round will be decided on basis of &#8216;machine being ahead in the 
      games&#8217; (see Rules 15-16 in Game Rules)<br>
      <br>
    </li>
    <li>If the scores are tied at the end of three rounds, a tie-breaker round 
      will be played to decide the winner.<br>
      <br>
    </li>
    <li>The team with maximum cumulative points after these 3 rounds (or 3 rounds 
      plus tie-breaker) will be awarded 1st position. The runner-up team will 
      be awarded 2nd position. A similar best-of-three round will be held amongst 
      the runner-up teams of the semi-finals to decide the 3rd position.<br>
      <br>
    </li>
  </ol>
  
</blockquote>
