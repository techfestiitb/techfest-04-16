<p>The Western Naval Command of the Indian Navy shall be putting on display
the cutting edge technology that has made the Indian Navy one of the
strongest and most respected forces world wide. The exhibition shall
highlight the four facets of a ship:-</p><br>

<ul><li><b>To Float: </b>What make these HUGE machines of war float? Hull design (all you're authorised to know), dry docking (repair) and more.<br></li><br>

<li><b>To Move:</b> Propulsion. Huge propellers (these ships may give a few
speedboats a run for their money). Engines the size of a small house.<br></li><br>
<li><b>
To Seek:</b> Radars, infra red, sensors. The works. Gadgets that serve as the
captains eyes in times of war.<br></li><br>

<li><b>To Destroy:</b> Guns, missiles, torpedos. This is how the navy takes its
enemies down. A rare opportunity to get a glimpse of these weapons.<br></li><br></ul>
<p>There will also be photographs, exhibits and banners depicting life in the
navy and various extracurricular activities. All in all an excellent opportunity to sneak a glimpse of what the navy is all about.
