<p>
<a name=avail><strong><font color=293932>How to avail accommodation</font></strong></a>
<br><br>&nbsp;<b>Participants:</b>
<ul>
<li>All<b> Participants</b> have to <a
href=main.php?file=base/registration.php class=greylink>register</a> for
their respective
competitions or workshops at www.techfest.org. All confirmed
<b>Participants</b> will then receive an  e-mail later    on to confirm their participation in the specific event. 
<br></li>
<li> You have to get the print out of this mail signed by the Dean/Head of your Institute and send it to us at the
 mentioned address at the earliest to avail accommodation. In case you
do not want to get accommodation, you need not send
the above mentioned letter. <br></li>
<li> There should be one and only one form per team.
 For teams taking part in more than one competition, only one form in sufficient. <br></li>
<li>Only those confirmed <b>Participants</b> whose letters are
received would be intimated of their confirmation of accommodation by a
<b>Confirmation of Accommodation</b> mail latest by 10th January'04. <br></li>
<li>For competitions/workshops where there is a pre-elimination through
abstracts/video, the final confirmed Participants will automatically
receive the above <b>Confirmation of Accommodation</b> mail after the
elimination results are declared. There is no need to send the letter
signed by the Head of your institute.<br></li>

<li>Please note that only those who have a printout of this<b> Confirmation of
Accommodation</b> mail will be provided accommodation.</li></ul>

&nbsp;<b>Visitors:</b>

<ul>
<li>To apply as <b>Visitors</b>, please <a class=greylink href=base/visitor_form.pdf target=_blank>
download and fill this form</a> and send it to us by
post/fax at the
address mentioned below. The same form has already been sent to
your college by post. Feel free to contact us for any further doubts.<br></li>
<li>Accommodation to visitors will be provided on a second preference after
participants as per availability. <br></li>
<li>All visitors whose accommodation is confirmed will receive a
<b>Confirmation of Accommodation</b> mail latest by 15th January'04.
Please note that only those who have a printout of this mail will be provided accommodation. <br></li>
<li>There should be only one form per institute for visitors.</li></ul>
</p>
