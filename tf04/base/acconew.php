<a name=assistance><strong><font color=#293932>Assistance Desk</font></strong></a><br><br>
Techfest endeavors to provide accommodation to as many people
as possible. Accommodation will be provided only to students/faculty of
institutes outside Mumbai.<br></p>

<p>Accommodation is provided on a priority basis first to
<b>Participants </b>and then to <b>Visitors</b>.<b>
Participants </b>are those who have a confirmation to
participate in any of the competitions or workshops. <b>Visitors </b>are those who do
 not have a confirmation for any of the competition or workshop.
Accommodation is provided on a first come first serve basis to
Visitors as per constraints.<br> </p>
<br>

<a name=travel ><strong><font color=293932>Travel Reimbursements</font></strong></a>
<p>Techfest shall not provide travel reimbursement to any team for the
journey from their home town to Bombay. However, transportation for the
teams from the major stations to the campus will be available free of
cost.</p><br>

<a name=trans><strong><font
color=293932>Transportation</font></strong></a>
<p>We shall have buses plying at regular intervals from the major stations
in Bombay to IIT Powai. The schedule of the buses shall be put up on the
website as the fest approaches. You can mail us your time of arrival in
Bombay to help us plan better.</p><br>
 <a name=how><strong><font color=293932>How to  get to  IIT
Powai</font></strong></a>
<p>To get to IIT Powai, just remember that <b>Kanjurmarg</b> is the
nearest
local
station and it lies on the Central Railway line.<br>
The address is simply <b>IIT Powai, Bombay . 400076.</b><br>

Following is a rough outline of how to get to IIT Powai from the major
train stations.<br><br>

Alight (preferably in order) at :
<ul><li>
<b>Dadar - </b> Take a slow train on the central line to Kanjurmarg
station
and then take an auto rickshaw to IIT (approx fare Rs.14/-).
</li><br><br>
<li><b>Bandra - </b> Take a fast local train to Dadar station from Bandra
local
station and follow the instructions in (a).</li><br><br>

<li><b>Bombay Central - </b> Take a local train to Andheri followed by
BEST  bus
(bus nos. 396, 392, 336) to IIT main gate from Andheri East OR take a
local train to Dadar and follow the instructions in (a).</li><br><br>

<li><b>Lokmanya Tilak Terminus (Kurla) - </b> Take an auto rickshaw to IIT
Main
Gate (approx fare 75-90) OR catch the slow local to Kanjurmarg from Kurla
local station followed by an auto rickshaw to IIT (approx fare Rs.
14/-).</li></ul>
