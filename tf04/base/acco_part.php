 
 
<p><font color=#293932><b>How to avail accommodation for Participants </b></font><br><ul>
<li>Techfest endeavors to provide accommodation to as many people
as possible. Accommodation will be provided only to students/faculty of
institutes outside Mumbai.<br></li>
<li>Participants </b>are those who have a confirmation to participate in any of the competitions or workshops.<br></li>

<li>All<b> Participants</b> have to <a 
href=../base/registration.html class=greylink>register</a> for 
their respective 
competitions or workshops at www.techfest.org. All confirmed 
<b>Participants</b> will then receive an  e-mail later    on to confirm their participation in the specific event. <br></li>
<li> You have to get the print out of this mail signed by the Dean/Head of your Institute and send it to us at the mentioned address at the earliest to avail accommodation. In case you do not want to get accommodation, you need not send the above mentioned letter. <br></li>
<li> There should be one and only one form per team. For teams taking part in more than one competition, only one form in sufficient. <br></li>
<li>Only those confirmed <b>Participants</b> whose letters are received would be intimated of their confirmation of accommodation by a  <b>Confirmation of Accommodation</b> mail latest by 10th January'04. <br></li>
<li>For competitions/workshops where there is a pre-elimination through
abstracts/video, the final confirmed Participants will automatically
receive the above <b>Confirmation of Accommodation</b> mail after the
elimination results are declared. There is no need to send the letter
signed by the Head of your institute.<br></li>

<li>Please note that only those who have a printout of this<b> Confirmation of  Accommodation</b> mail will be provided accommodation.<br></li>
<li>You must also bring your college identity cards in order to avail
accommodation. Please note that no other document (a letter or otherwise)
will be accepted.</li><br>
<li>Accommodation will be provided from 6:00 a.m. on 23th January'04
to 12:00 noon of 27th January'04.</li><br>
<li>Accommodation will be provided on a sharing basis.</li><br>
<li>A refundable security deposit and nominal fees may be charged per
person for the accommodation provided. Details regarding this exact amount
will be available on the website soon.</li></p></ul>


