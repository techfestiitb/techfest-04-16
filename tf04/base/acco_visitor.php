<p><font color=#293932><b>How to avail accommodation for Visitors</font></b><ul>
<li>Techfest endeavors to provide accommodation to as many people
as possible. Accommodation will be provided only to students/faculty of
institutes outside Mumbai.<br></li>

<li>To apply as <b>Visitors</b>, 

Please <a class=greylink href=base/visitor_form.pdf target=_blank>download and fill this form</a> and send it to us by post/fax at the
address mentioned below. The same form has already been sent to
your college by post. Feel free to contact us for any further doubts.<br></li>
<li>Accommodation to visitors will be provided on a second preference after 
participants as per availability. <br></li>
<li>All visitors whose accommodation is confirmed will receive a <b>Confirmation of Accommodation</b> mail latest by 15th January'04. Please note that only those who have a printout of this mail will be provided accommodation. <br></li>
<li>There should be only one form per institute for visitors.<br></li>
<li>You must also bring your college identity cards in order to avail
accommodation. Please note that no other document (a letter or otherwise)
will be accepted.</li><br>
<li>Accommodation will be provided from 6:00 a.m. on 23th January'04
to 12:00 noon of 27th January'04.</li><br>
<li>Accommodation will be provided on a sharing basis.</li><br>
<li>A refundable security deposit and nominal fees may be charged per
person for the accommodation provided. Details regarding this exact amount
will be available on the website soon.</li></p></ul>


