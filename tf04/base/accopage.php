Following is a rough outline of how to get to IIT Powai from the major 
train stations.<br><br>

Alight (preferably in order) at :
<ul><li>
<b>Dadar - </b> Take a slow train on the central line to Kanjurmarg 
station 
and then take an auto rickshaw to IIT (approx fare Rs.14/-).
</li><br><br>
<li><b>Bandra - </b> Take a fast local train to Dadar station from Bandra 
local 
station and follow the instructions in (a).</li><br><br>

<li><b>Bombay Central - </b> Take a local train to Andheri followed by 
BEST  bus 
(bus nos. 396, 392, 336) to IIT main gate from Andheri East OR take a 
local train to Dadar and follow the instructions in (a).</li><br><br> 

<li><b>Lokmanya Tilak Terminus (Kurla) - </b> Take an auto rickshaw to IIT 
Main 
