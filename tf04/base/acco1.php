<p>Techfest endeavors to provide accommodation to as many people as possible. Accommodation will be provided only to students/faculty of institutes outside Mumbai.<br></p> 
 
<p>Accommodation is provided on a priority basis first to <b>Participants </b>and then to <b>Visitors</b>.<b> Participants </b>are those who have a confirmation to participate in any of the competitions or workshops. <b>Visitors </b>are those who do not have a confirmation for any of the competition or workshop. Accommodation is provided on a first come first serve basis to Visitors as per constraints.<br> </p>
 
<p><b>How to avail accommodation for Participants </b><br><ul>
<li>All<b> Participants</b> have to register for their respective competitions or workshops at <a class=greylink href=http://www.techfest.org target=mainFrame>www.techfest.org</a>. All confirmed <b>Participants</b> will then receive an  e-mail later on to confirm their participation in the specific event. <br></li>
<li> For competitions/workshops where there is a pre-elimination through abstracts/ video, the final confirmed Participants will automatically receive the above mail after the elimination results are declared. <br></li>
<li> You have to get the print out of this mail signed by the Dean/Head of your Institute and send it to us at the mentioned address at the earliest to avail accommodation. In case you do not want to get accommodation, you need not send the above mentioned letter. <br></li>
<li> There should be one and only one form per team. For teams taking part in more than one competition, only one form in sufficient. <br></li>
<li>Only those confirmed <b>Participants</b> whose letters are received would be intimated of their confirmation of accommodation by a  <b>Confirmation of Accommodation</b> mail latest by 10th January'2004. <br></li>
<li>Please note that only those who have a printout of this<b> 
Confirmation of  Accommodation</b> mail will be provided 
