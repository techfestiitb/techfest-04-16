The objective of this competition is to build a structure
that can
lift heavy loads to a given height (+/- the tolerance limit) using
chemical energy. The lifts will be judged based on the maximum load they
can lift in a pre-defined number of trials. Importance will be also be
given to the creative and safety aspects of the lifts.
