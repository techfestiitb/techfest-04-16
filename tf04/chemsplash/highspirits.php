<ol> 

<li>Each lift will be given one opportunity per round to rise to a specified height carrying different loads. The height
to be achieved by the lift is 2ft±0.5ft for the performance competition.<br><br></li>

<li>Teams may not add or remove any 'load' (or other inert items) to adjust their lift's weight once the poster
presentation has concluded. Teams are allowed to adjust 'fuel' or reactants used in the lift's chemical 
reaction.<br><br>
</li>
 
<li>The lift will start with its pan just touching a designated starting point. There will be a vertical scale which has
to be attached to the setup. The height that the lift rises to would be observed on this scale. The distance will be
measured with respect to the top most the pan.<br><br></li>


<li>A lift that goes outside the bounds (i.e. more/less than 6 inches of the specified height) will have its load lifted calculated as:<br>
 <img src="chemsplash/clip.gif" alt="formula"/><br><br></li>
  
<li>An objective of this contest is the demonstration of the ability to control a chemical reaction. The only energy
source for the propulsion of the lift is a chemical reaction. Commercial batteries (for example, AA batteries),
Commercial engines (combustion engines) are not allowed as the power source.<br><br> </li><li>The lift must be an
autonomous vehicle and cannot be controlled remotely.<b> Pushing to start the lift or a mechanical starting device is
not allowed.</b> There can be no electronic timing device(s) to stop the chemical reaction.
<br><br></li>

<li>The lift must carry a pan that holds the weights. At the competition, only the weights will be supplied, thus each
lift must already have its own pan. The size of the pan should at least be 15cms x 15 cms. Weight of the pan will not be
considered at the time of calculation of maximum weight lifted by the lift.<br><br></li>

<li>Students are responsible for transportation of the chemicals to the competition site. Students are also responsible
for arranging for the disposal of their chemicals and wastes. The possibility of on-site waste disposal will be posted
here, if available.<br><br></li>

<li>The poster display judging will occur prior to the lift performance competition. Team members should be present
during judging to answer questions from the judges.<br><br></li> 

<li>A poster board must be displayed with the setup on the day of the competition. This poster should describe how
the lift is powered using the chemical reaction, the unique features of the lift, and environmental and safety features
in the design. Material Safety Data Sheets (MSDS) must be present for each chemical used by the entry.<br><br></li>


<li>The poster display will carry a weightage of
20%. Posters will be judged according to the following criteria, with a maximum of 20 possible points (equal weighting): 
<ul>
<li>Description of the chemical reaction / power source .</li><br>
<li>Unique features of the lift.</li><br>
<li>Environmental and safety features. </li></ul><br>
</li>


