<p><b><a name=safenorms><font color="#293932">SAFETY NORMS:</a></font></b></p> <ol> <li>Safety and safe transportation
procedures are important items to address. Hazardous chemical protocols must be followed and reported on the poster.
Each team must have a MSDS for each chemical it is using. If obvious safety violations have occurred, the judges have
the discretion to disqualify the entry.<br><br></li>

<li>If a lift is deemed unsafe, then the judges may disqualify it. Given the general public's lack of understanding of
general chemistry, anything that is visibly left behind, may well be construed as chemical pollution, and even
hazardous. Any entry using or producing a corrosive chemical(s) must have the chemical(s) sealed in a container, or at
least use a spill-proof container.<br><br></li>

<li>Appropriate personal protection must be worn by the team members operating the lift. All lifts must safely operate
inside a building.</li><br> <li>Chemicals <b>must not</b> be stored in hostel rooms. Check here for the latest
information on where chemicals may be stored at the competition site: <a href=main.php?file=chemsplash/chemsplash.php
class=greylink>www.techfest.org/chemsplash</a>.<br><br></li><li> In order to facilitate chemical safety at the
competition site, a designated area will be identified where teams must mix or prepare their chemicals (unless the
materials are pre-mixed). Unfortunately, due to hostel safety regulations it is not possible to allow teams to do "trial
runs" in the hostels. Teams that violate these safety rules jeopardize the continued operation of High Spirits
Competition and will be disqualified.<br><br></li>
	</ol><br><br>
