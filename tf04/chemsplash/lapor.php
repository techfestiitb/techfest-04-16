<a name=car><font color=#1f3d55><b>CAR PEROFORAMNCE 
COMPETITION:</b></font></a><br><br>
<ol>
  <li>Each car will be given two opportunities to traverse a specified distance 
    carrying a certain fixed additional load. The required distance will be given 
    to each team one hour prior to the start of the performance competition. The 
    distance will be between 15 and 30 ft ± 0.5 in. and the load will be 200 ml 
    of water. A judge will measure out the prescribed water for each team. Teams 
    may not add or remove any "load" (or other inert items) to adjust their vehicle 
    weight once the poster session has concluded. Teams are allowed to adjust 
    "fuel" or reactants used in the car's chemical reaction.<br>
    <br>
  </li>
  <li>The car will start with its front end just touching the designated starting 
    line. There will be a designated finish line. The distance will be measured 
    with respect to the front most point of the car. The goal of the competition 
    is to have your car stop closest to the specified finish line (in bounds) 
    while carrying the specified load. The course will be rectangular shaped with 
    a starting line and the prescribed distance clearly marked. Physical site 
    will dictate the exact course layout. A vehicle that goes outside the course 
    will have its distance measured to where it went out of bounds and will receive 
    a penalty of 5ft. added to the measured distance. When measuring the distance 
    from the finish line it does not matter if the car goes longer or shorter 
    than the prescribed distance. <br>
    <br>
  </li>
  <li>A judge will announce each team just prior to the start of their run. The 
    team then has 1 minute to get to the starting line. Each team will be asked 
    to introduce their entry to the audience (College name and briefly mention 
    your propulsion system). Each car will have 2 attempts to complete the course, 
    each attempt lasting no more than 2 minutes. The best score of these two attempts 
    will be used in the judging. The next team to compete should be ready and 
    at the staging area at least 5 minutes before their anticipated run time. 
    In the event a team fails to show up on the start line, or the vehicle fails 
    to start, the next team on the competition order list will be announced and 
    requested to proceed to the starting line. In the first round of competition 
    the order of the teams will be by random drawing. At the completion of the 
    first round there will be a short break before the second round begins. The 
    competition order in the second round will be determined by the 1st round 
    standings, beginning with the team that is farthest from the prescribed distance 
    and ending with the team that was closest. <br>
    <br>
  </li>
  <li>Ties will be decided by the best average from the two runs completed.<br>
    <br>
  </li>
  <li>An objective of this contest is the demonstration of the ability to control 
    a chemical reaction. The only energy source for the propulsion of the car 
    is a chemical reaction. Commercial batteries (for example, AA batteries) are 
    not allowed as the power source. <br>
    <br>
  </li>
  <li>The car must be an autonomous vehicle and cannot be controlled remotely. 
    Pushing to start the vehicle or a mechanical starting device is not allowed. 
    Check with the Rules Coordinators if you have a specific question concerning 
    your vehicle.<br>
    <br>
  </li>
  <li>No mechanical force can be applied to the wheel or ground to slow or stop 
    the car (e.g. no brakes).<br>
    <br>
  </li>
  <li>There can be no mechanical or electronic timing device(s) to stop the chemical 
    reaction.<br>
    <br>
  </li>
  <li>All components of the car must fit into a shoebox of dimensions no larger 
    than 40 cm x 30 cm x 18 cm. The car may be disassembled to meet this requirement. 
    If the judges are uncertain whether the car will fit inside the box when dissembled, 
    they may request that the team demonstrate that they can do this.<br>
    <br>
  </li>
  <li>The car must carry a container that holds up to 200 ml of water without 
    spilling. At the competition, only the water will be supplied, thus each car 
    must already have its own container.<br>
    <br>
  </li>
  <li>The cost of the contents of the "shoe box" and the chemicals must not exceed 
    Rs 750.<br>
    <br>
  </li>
  <li>Students are responsible for transportation of the chemicals to the competition 
    sites. Students are also responsible for arranging for the disposal of their 
    chemicals and wastes. The possibility of on-site waste disposal will be posted 
    here, if available. Hazardous chemical protocols must be followed and reported 
    on the poster. Each team must have a MSDS for each chemical it is using. If 
    obvious safety violations have occurred, the judges have the discretion to 
    disqualify the entry.<br>
    <br>
  </li>
  <li>Appropriate personal protection must be worn by those team members operating 
    the vehicle. All cars must safely operate inside a building. If a car is deemed 
    unsafe, then the judges may disqualify it. Given the general public's lack 
    of understanding of general chemistry, anything that is visibly left behind 
    may well be construed as chemical pollution, and even hazardous. Any entry 
    using or producing a corrosive chemical(s) must have the chemical(s) sealed 
    in a container, or at least use a spill-proof container. If there is any uncertainty 
    on an issue of safety or other judging criteria, please contact:<br>
