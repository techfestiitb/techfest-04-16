  </li>
  <li>Chemicals must not be stored in hostel rooms. In order to facilitate chemical 
    safety at the competition site, a designated area will be identified where 
    teams must mix or prepare their chemicals (unless the material are pre-mixed). 
    All chemicals will be made available to the teams in the chemical preparation 
    area at least 2 hours prior to the performance competition. Unfortunately, 
    due to hostel safety regulations it is not possible to allow teams to do "trial 
    runs" in the hostels. Teams that violate these safety rules jeopardize the 
    continued operation of la porsChe Competition and may be disqualified by the 
    judges.<br>
    <br>
  </li>
  <li>The competition will be conducted on the honor system. Faculty and graduate 
    students can only act as sounding boards to the student queries. The faculty 
    cannot be idea generators for the project.<br>
    <br> </li>
  <li>The maximum team size is four participants. All team members do not have 
    to be present at the competition; however, all are encouraged to try and attend.<br>
    <br>
  </li>
  <li>Four new categories have been created. The award criteria will be as follows:<br>
    <strong> Most Consistent Performance</strong>This award is based on the best 
    average score for the two runs that the vehicle makes. It has been created 
    to recognize the team that has designed and most understands the performance 
    of the reaction that powers the vehicle.<br>
    <strong>Spirit of the Competition </strong>- This award is given to the team displaying the 
    most team spirit as decided by a panel of judges.<br>
    <strong>Most Creative Drive System</strong> - Recognition is awarded to the team that has designed 
    and installed the most creative propulsion system. The winner is decided by 
    a panel of judges during the poster competition. <br>
   <strong> Most creative vehicle design</strong>- The winning entry is decided by a ballot cast 
    by people watching the competition.<br>
  </li>
