<h1>Machine Specifications</h1>
<p><ul><li>The machine should fit within a box of dimensions (25*25*25) cm at the start of the game. The external device which is used to control the machine manually is not included in the size constraint.</li>
 <li>The machine must use power converted from a source of electrical energy. Mechanical force/power should not be transferred to the machine from outside. Chemicals, compressed gas and rockets, fuels, combustion power methods, etc. are strictly prohibited. However commercial batteries like dry cells and lead acid cells are permitted.</li>
<li>The machine may or may not have an onboard source of power. External source of electrical power is permitted. </li>
</ul></p>
<a name="grules"></a>
<h1>Rules of the Game</h1>
<p><ul>
<li>The teams are allowed to manually engage the machine to the pipes at the start of the game.</li>
<li>There is no restriction on the size of the machine after the machine has been checked for dimensions.</li>
<li>The rear part of the machine should touch Region A at the start of the game. The machine has to transverse through the pipes to reach the Zone A.</li>
<li>The machine has to then pick up a cylindrical object which can be initially placed anywhere in the Zone A by the team. </li>
<li>The machine can use any mechanism whatsoever to pick up the object. However chemicals, compressed gas and rockets, fuels, combustion power methods are strictly prohibited. </li>
<li>The machine has to then move backwards carrying the cylinder and place it in one of the zones provided.</li>
<li>No part of the cylinder must touch the ground while the machine is moving to the other end.</li>
<li>While placing the cylinder in the zones provided no part of the machine should be in contact with the Region B or the Region A.</li>
<li>The cylinder once in stable contact with the ground will be considered as the final drop position; no further change in the drop position of the cylinder will be entertained.</li>
<li>In case the machine drops the cylinder before entering the zonal region the cylinder can be placed again at the initial position but with certain penalty of points(refer to points structure). </li>
<li>After the machine places the cylinder in the zone, another identical cylinder will be placed in the ZONE A by one of the team members. The machine has to now come back to Region B pick up the cylinder and follow the same procedure as before.  </li>
<li>While the machine is in the process of picking up the new identical cylinder, the previously placed cylinder will be removed from the zonal region.</li>
<li>The machine can lift only one cylinder at a time.</li>
<li>In case the machine falls off the rails during any point of the game, it can be manually engaged to the initial position but with certain penalty of points (refer to points structure). </li>
<li>The clock will not be stopped at any instant of the game.</li>
<li>The time limit for each team is 5 minutes .At the end of 5 minutes the machine which has most number of points, after taking into account the penalties is declared as the winner. But in case of equal points there with be rematch(es).</li>
<li> The machine should NOT be made using Lego parts, assemblies from toys etc. However, the remote control systems from toys can be used. Also, standard kits like the Basic Stamp kits may be used. Please note that readymade wheels and gearboxes may be used.</li>
</ul></p>
<a name="point"></a>
<h1>Points Structure</h1>
<p><strong>Traversing from one end to the other +50 points.</strong> There will be no points for traversing from Region B to Region A without the cylinder. The points for traversing from Region A to and B will be given only if the machine has placed the cylinder in one of the zones in the prior round (except the first run).</p>
<p><strong>Picking up a cylinder +100 points.</strong> Picking up means no part of the cylinder must be in contact with the ground. The points will be given only once for each round.</p>
<strong>Points for placing the cylinders</strong>
