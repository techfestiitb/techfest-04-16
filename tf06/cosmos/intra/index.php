<p>For gaining points for a particular zone the cylinder must be completely placed within the zone. If any part of the cylinder even touches the boundary line, points awarded will be that corresponding to the next zone.</p>
<p>In case of any ambiguity regarding the point structure the Judges’ discretion will be used</p>
<h3>Penalty</h3>
<p>50 points will be deducted if </p>
1. Machine falls off the track<br>
2. The machine is unable to move and the team wishes to restart.<br>
<p>In each case the machine will be put to the starting position and the clock will never be stopped.</p>
<p>If the cylinder comes in contact with the ground while the machine is traversing from one region to the other the cylinder will be placed in the ZONE A and the machine will have to go back and lift the cylinder again. 50 points will also be deducted. </p>
<p>However if the cylinder touches the ground in ZONE A there is no penalty, but the cylinder will again be placed in ZONE A. The clock will never be stopped in any condition. </p>
<p>In case the cylinder falls down in the zonal region (Zone 1, 2, 3), there is no penalty and the points will be given only if it becomes stationary in one of the zones. </p>
<a name="gnrules"></a>
<h1>General Rules</h1>
<h3>Power Supply</h3>
<p>Normal 220 ± 2% volt 50 Hz AC extension boards will be available at the venue. You should fetch your own eliminators/adaptors. No specialized demand should be made. Use of heavy equipment like compressors is banned. The power cords connecting a machine to the power supply must be slack at all times. Likewise the Control Cords must also be slack. Thus, please ensure that all cords are fairly long. Use of devices which can transfer manual power to machine directly is not permitted, for example use of clutch wires or brake wires is not permitted.</p>
<h3> Safety</h3>
<p>The machines you build will be first checked for size and safety. The machine should not cause electrical power breakdowns. The robots must not contain any combustible, corrosive, or otherwise dangerous materials. No explosive, compressive or expansive, either internal or external is permitted. There is a risk of compressed containers rupturing and creating shrapnel therefore they are not allowed. Any robot whose strategy or operation is considered dangerous can be disqualified by the judges at their discretion.</p>
<h3>Fouls/Penalties</h3>
<p><ol type="1">
<li>Team members are not permitted to touch either their machines (except for the case when the participant is engaging the machine or the cylinder). The penalty for doing so is disqualification on judges’ discretion. No addition or removal of any part(s) is allowed in any of the stages.</li>
<li>The robots are not allowed to leave part(s) on any part of the arena. In case some part of the machine falls on the arena, the penalty (including disqualification) will be based on judges’ discretion.</li>
<li>Machines damaging any part of the arena will be disqualified. Damaging does not include unintentional scratch(es) on the painted surface by the machine. Once again judges’ discretion will be used.</li>
<li>During the game run if a machine is static continuously for a minute than the machine is disqualified</li>
</ol></p>
