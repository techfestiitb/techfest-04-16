<h1 id="toptext">G.R.I.P. - Intra</h1> 
<p>Make a wired/wireless remote controlled machine, which can traverse on two parallel pipes 50 cm high, collect a cylindrical object which is located in a specific zone below the pipes, and then place the cylinder in the zones provided on the other end. The machine which scores the maximum points is declared the winner. </p><a name="prob"></a>
<p><strong>UPDATE:</strong> The intra competition has been postponed to <strong>11<sup>th</sup> November, 2005.</strong></p>
<a name="arena"></a>
<h1>The Game Arena</h1>
<p>There are two metal pipes each 120 cm long at a height of 50 cm from the ground and separated by a distance of 40 cm. The pipes are made of standard GI pipes of nominal diameter 0.75 inch and external diameter 26.5 ± 1 mm, coated with oil paint. Initially the cylinder can be placed anywhere in the ZONE A manually. The cylinder then has to be picked up by the machine and carried to the other end of the pipes and placed in one of the three zones (ZONES 1,2,3). Points will be awarded for various stages of the game.</p>
