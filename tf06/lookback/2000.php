<h3>Workshops</h3>

<p>The following workshops were held as a part of Techfest 2000:</p>

<p><b>quEsT: The SETI Workshop </b><br>
ARE WE AlONE? - quEsT the SETI workshop was structured so as to provide the participants an insight into the principles and practices involved in the Search for Extra Terrestrial Intelligence. The aim was to broaden horizons and help the participants better understand and appreciate the philosophy, rationale and techniques of SETI.<br>
Eminent personalities present included Dr. H. Paul Shuch, Executive Director of the SETI League, better known as Dr. SETI, Dr. Govind Swarup and Dr. Anantakrishnan from the National Centre for Radio Astrophysics, Dr. S. M. Chitre, from the Department of Astronomy and Astrphysics, TIFR and Dr. S. Durani, Head, Biotech Department, IIT Bombay. </p>

<p><b>Mesmerize </b><br>
The aim of the workshop was to provide a glimpse of the world of animation and special effects. It included a demonstration (courtesy Discrete Logic) of the various software and hardware commonly used for animation and special effect techniques like morphing, editing, sound mixing etc. The total cost of the setup was in excess of 1.7 crore rupees. There was also a hands on session (courtesy 3-D Studio Max) where the participants got to use the various softwares. </p>

<p><b>Hardcore Hardware</b><br>
This workshop was meant to give a working knowledge of computer hardware. Starting from the very basics of computer systems, this workshop intended to provide solutions to most of the problems one faces as a user, and culminated with an introduction to computer networks.</p>


