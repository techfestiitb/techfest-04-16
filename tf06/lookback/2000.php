<h3>Eureka</h3>


<p>Eureka, the entrepreneurship competition was one of the most successful events at Techfest 2000. It was a competition where the teams were expected to develop well researched, feasible and of course profitable business plans. The aim of holding this competition was to increase interest in entrepreneurship in the student community.</p>
